#include "3-calc.h"

/**
 * op_add - calculate the sum of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calculate the difference of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: difference of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - calculate the the product of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: product if a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - calculate the division of two integer
 * @a: first integer
 * @b: second integer
 *
 * Return: division of a and b
 */

int op_div(int a, int b);
{
	return (a / b);
}

/**
 * op_mod - calculate the module of two integer
 * @a: first integer
 * @b: seconf integer
 *
 * Return: module of a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}

